/**
 * op_add - addition
 *
 * @a: input uno
 * @b: input dos
 *
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction
 *
 * @a: input uno
 * @b: input dos
 *
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: input uno
 * @b: input dos
 *
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: input uno
 * @b: input dos
 *
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 *
 * @a: input uno
 * @b: input dos
 *
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}







